# Definindo variáveis para armazenar 4 números
numero_1 = 10
numero_2 = 20
numero_3 = 30
numero_4 = 40

# Definindo uma variável para armazenar uma frase
frase = "Python é uma linguagem de programação poderosa e versátil."

# Definindo uma variável para armazenar uma palavra
palavra = "programação"

# Calculando a média aritmética entre os quatro números
media = (numero_1 + numero_2 + numero_3 + numero_4) / 4

# Calculando o quadrado de um dos números (vamos escolher o número_3)
quadrado_numero = numero_3 ** 2

# Calculando o dobro de um dos números (vamos escolher o número_4)
dobro_numero = numero_4 * 2

# Contando a quantidade de letras na palavra
quantidade_letras = len(palavra)

# Contando a quantidade de espaços em branco na frase
quantidade_espacos = frase.count(" ")

# Verificando se o primeiro número é maior que o segundo
primeiro_maior_que_segundo = numero_1 > numero_2

# Encontrando o maior número entre os quatro números
maior_numero = max(numero_1, numero_2, numero_3, numero_4)

# Imprimindo os resultados
print("Média aritmética dos números:", media)
print("Quadrado de um dos números (numero_3):", quadrado_numero)
print("O dobro de um dos números (numero_4):", dobro_numero)
print("Quantidade de letras na palavra:", quantidade_letras)
print("Quantidade de espaços em branco na frase:", quantidade_espacos)
print("O primeiro número é maior que o segundo?", primeiro_maior_que_segundo)
print("O maior número entre os quatro é:", maior_numero)